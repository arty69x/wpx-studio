function node(id: string, type: StructureNode['type'], label: string, role: string, children: StructureNode[] = []): StructureNode {
  return { id, type, label, role, children };
}

function makeStructure(name: string, previewPattern: PreviewPattern): StructureNode {
  const base = name.toLowerCase().replace(/[^a-z0-9]+/g, '-');
  const previewChildren: StructureNode[] = {
    carousel: [
      node(`${base}-track`, 'element', 'Element: Track', 'Animated slide rail'),
      node(`${base}-slide`, 'element', 'Element: Featured Slide', 'Active visual item'),
      node(`${base}-depth`, 'micro-div', 'Micro Div: Perspective Layer', '3D depth isolation'),
    ],
    navigation: [
      node(`${base}-brand`, 'element', 'Element: Brand', 'Navigation identity'),
      node(`${base}-links`, 'element', 'Element: Link Group', 'Primary route links'),
      node(`${base}-indicator`, 'micro-div', 'Micro Div: Active Indicator', 'Animated selection highlight'),
    ],
    typography: [
      node(`${base}-text`, 'element', 'Element: Text Glyphs', 'Animated copy primitive'),
      node(`${base}-mask`, 'micro-div', 'Micro Div: Reveal Mask', 'Text motion clipping layer'),
    ],
    layout: [
      node(`${base}-tile`, 'element', 'Element: Layout Tile', 'Responsive content tile'),
      node(`${base}-overlay`, 'micro-div', 'Micro Div: Glass Overlay', 'Depth and contrast layer'),
    ],
    background: [
      node(`${base}-field`, 'element', 'Element: Background Field', 'Rendered background primitive'),
      node(`${base}-grain`, 'micro-div', 'Micro Div: Texture Overlay', 'Decorative background texture'),
    ],
    interaction: [
      node(`${base}-target`, 'element', 'Element: Interaction Target', 'Hover/focus target'),
      node(`${base}-feedback`, 'micro-div', 'Micro Div: Feedback Glow', 'Isolated interaction feedback'),
    ],
  }[previewPattern];

  const contentNode = node(`${base}-content`, 'node', 'Node: Content', 'Metadata and readable labels', [
    node(`${base}-title`, 'element', 'Element: Title', 'Rendered component title'),
    node(`${base}-tags`, 'element', 'Element: Tags', 'Rendered tag group'),
  ]);

  const actionNode = node(`${base}-actions`, 'node', 'Node: Actions', 'Preview, save, and export controls', [
    node(`${base}-preview-button`, 'element', 'Element: Preview Button', 'Preview action'),
    node(`${base}-save-button`, 'element', 'Element: Save Button', 'Save action'),
    node(`${base}-export-button`, 'element', 'Element: Export Button', 'Export action'),
  ]);

  const component = node(`${base}-component`, 'component', name, `${previewPattern} reusable component`, [
    node(`${base}-preview`, 'node', `Node: ${previewPattern} Preview`, 'Pattern-specific preview anatomy', previewChildren),
    contentNode,
    actionNode,
  ]);

  return node(`${base}-section`, 'section', 'Section', 'Semantic marketplace preview section', [
    node(`${base}-container`, 'container', 'Container', 'Max width, padding, and breakpoint behavior', [
      node(`${base}-grid`, 'grid', 'Grid', 'Two-dimensional placement system', [
        node(`${base}-row`, 'row', 'Row', 'Horizontal grouping inside grid', [
          node(`${base}-column-a`, 'column', 'Column: Preview', 'Primary preview column', [component]),
          node(`${base}-column-b`, 'column', 'Column: Meta Stack', 'Supporting metadata column', [
            node(`${base}-stack`, 'stack', 'Stack', 'One-dimensional metadata flow'),
          ]),
        ]),
      ]),
    ]),
  ]);
}

